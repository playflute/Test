
public class Step {
	private int row;
	private int column;
	private String color;
	
	public Step(int row, int column,String color) {
		super();
		this.row = row;
		this.column = column;
		this.color=color;
	}

	public int getRow() {
		return row;
	}

	public int getColumn() {
		return column;
	}

	public String getColor() {
		return color;
	}
	
	

}
